2. **"他们有问题"** ——先解释为什么不是我的责任，而不是先做点什么。
   - 应用场景：被质疑/结果不好的时候
   - 案例：游戏输了是英雄的问题，补偿没拿到是老板不诚信的问题

3. **"别人比我更坏"** ——对方有我的把柄，对方比我强，对方有资源，所以我动不了他们。
   - 应用场景：需要衡量双方实力时
